/* Simple Math Calculator */

//Addition
module.exports.add = function(i, j) {
    return i + j;
};

// Subtraction
module.exports.sub = function (i,j) {
 	return i-j;
};
//Multiplication
module.exports.mul = function(i, j) {
    return i * j;
};
// Division
module.exports.div = function (i,j) {
	return i/j;
};
